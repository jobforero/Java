package estructuraDecisiones;

public class valorIgual {

	public static void main(String args[]) {

		int numero = 2;

		switch (numero) {
		case 1:
			System.out.println("Uno");
			break;
		case 2:
			System.out.println("Dos");
			break;
		case 3:
			System.out.println("Tres");
			break;
		default:
			System.out.println("Otro número");
		}
	}

}